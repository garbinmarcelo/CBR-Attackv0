    public JAttributeListener(JFrame parent, Attribute attribute, ArrayList<String> attributes) {
        initComponents();
        this.jLabel.setText(attribute.name);       
        jList.setModel(new DefaultListModel<>());
        DefaultListModel listModel = (DefaultListModel) jList.getModel();
        listModel.clear();
        if(attributes != null){
            attributes.forEach((a) -> {
                listModel.addElement(a);
            });
            jList.setModel(listModel);
        }
        this.attribute = attribute;
        this.parent = parent;        
    }
    
    private void search(){
        JAttributes jAttributes = new JAttributes(parent, true, attribute);
        jAttributes.setVisible(true);
        jTextField.setText(jAttributes.getValue() != null ? jAttributes.getValue() :jTextField.getText());
        if(jTextField.getText().trim().isEmpty()){
            jButton_Search.requestFocus();
        } else {
            jButton_Add.requestFocus();
        }
    }
    
