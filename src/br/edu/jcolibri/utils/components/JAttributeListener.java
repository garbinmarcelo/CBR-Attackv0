  public void addList(ArrayList<String> useCasesRelationship){
        if (useCasesRelationship == null) {
            useCasesRelationship = new ArrayList<>();
        }
        jList.removeAll();
        DefaultListModel listModel = new DefaultListModel();
        for (String string : useCasesRelationship) {
            listModel.addElement(string);
        }
        jList.setModel(listModel);
  }
  
  public void addValue(String value){
      DefaultListModel dlm = (DefaultListModel) jList.getModel();
      dlm.addElement(value);
  }
  
  private void addValue(){
      if(jTextField.getText().trim().isEmpty()){
          return;
      }
      DefaultListModel dlm = (DefaultListModel) jList.getModel();
      for (int i = 0; i < dlm.size(); i++) {
          if(dlm.getElementAt(i).equals(jTextField.getText())){
              return;
          }
      }
      dlm.addElement(jTextField.getText());
      jTextField.setText(null);
      jButton_Search.requestFocus();
  }
  
  private void removeValue(){
        DefaultListModel dlm = (DefaultListModel) jList.getModel();
        dlm.remove(jList.getSelectedIndex());
  }
  
  public JList getJList(){
      return this.jList;
  }
   
